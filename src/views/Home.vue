<template>
  <div>
    <div class="box">🔔{{billboard.userName+",你好！"}}</div>
  </div>
</template>

<script>
import{getBillboard} from '@/api/billboard'
export default {
  name: 'Home',
  data(){
    return{
      billboard:{
        userName:''
      }
    }
  },
  created(){
    this.fetchBillboard()
  },
  methods:{
    async fetchBillboard(){
      getBillboard().then((value) => {
        const { data } = value;
        this.billboard = data
      }
      )
    }
  }
}
</script>
